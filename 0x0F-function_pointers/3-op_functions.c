/**
 * op_add - add two integers
 *@a: the first int
 *@b: the second int
 *
 * Return: int the result
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract b from a
 *@a: the first int
 *@b: the second int
 *
 * Return: int the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply two integers
 *@a: the first int
 *@b: the second int
 *
 * Return: int the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divide a by b
 *@a: the first int
 *@b: the second int
 *
 * Return: int the result
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - calculate the reminder of dividing a by b
 *@a: the first int
 *@b: the second int
 *
 * Return: int the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}


